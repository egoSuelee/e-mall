+(UIImage *) imageStretch:(UIImage *)img{
    return [img stretchableImageWithLeftCapWidth:floorf(img.size.width/2) topCapHeight:floorf(img.size.height/2)];
}

+(UIImage *) imageStretch:(UIImage *)img withEdge:(UIEdgeInsets)edge{
    //UIImageResizingModeStretch：拉伸模式，通过拉伸UIEdgeInsets指定的矩形区域来填充图片
    //UIImageResizingModeTile：平铺模式，通过重复显示UIEdgeInsets指定的矩形区域来填充图
    img= [img resizableImageWithCapInsets:edge resizingMode:UIImageResizingModeStretch];
    return img;

}


